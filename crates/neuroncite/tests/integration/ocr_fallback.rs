// ---------------------------------------------------------------------------
// T-OCR-013: Auto-extract produces pages with recognized backends
// ---------------------------------------------------------------------------

/// T-OCR-013: Verifies that the auto-extract chain (backend=None) produces
/// non-empty pages for all three problematic PDFs and that every page is
/// annotated with a recognized backend (PdfExtract, Pdfium, or Ocr). Mixed
/// backends within a single file are expected because the per-page quality
/// checks route pages individually through the fallback chain.
#[test]
fn t_ocr_013_auto_extract_uses_pdfium_backend() {
    assert!(
        is_pdfium_available(),
        "pdfium library is required for this test. Install pdfium via the Doctor panel.",
    );

    let paths = match ocr_pdf_paths() {
        Some(p) => p,
        None => {
            eprintln!("skipping: OCR test PDFs not found. Run: cargo run -p neuroncite-testgen");
            return;
        }
    };
    let mut files_checked = 0_usize;

    for pdf_path in &paths {
        let pages = match extract_with_timeout_and_backend(pdf_path, EXTRACT_TIMEOUT, None) {
            Some(p) if !p.is_empty() => p,
            _ => continue,
        };

        // Every page must have been extracted by a recognized backend
        // (PdfExtract, Pdfium, or Ocr). Mixed backends within a single
        // file are valid because the auto-extract chain applies per-page
        // quality checks: pages with sufficient embedded text keep their
        // PdfExtract or Pdfium backend, while pages with sparse text fall
        // through to OCR. This per-page fallback produces higher overall
        // text quality than forcing a single backend for the entire file.
        for page in &pages {
            assert!(
                matches!(
                    page.backend,
                    ExtractionBackend::PdfExtract
                        | ExtractionBackend::Pdfium
                        | ExtractionBackend::Ocr
                ),
                "page {} of {} has unexpected backend {:?}",
                page.page_number,
                pdf_path.file_name().unwrap_or_default().to_string_lossy(),
                page.backend
            );
        }

        files_checked += 1;
    }

    assert!(
        files_checked >= 2,
        "at least 2 of 3 problematic PDFs must be verified, got {files_checked}"
    );
}

// ---------------------------------------------------------------------------
// T-OCR-020: Tesseract CLI OCR produces text from a rendered page
// ---------------------------------------------------------------------------

/// T-OCR-020: Verifies that the Tesseract CLI subprocess OCR pipeline works
/// end-to-end. Creates a minimal PNG image containing a white pixel, invokes
/// `neuroncite_pdf::ocr::ocr_page()` which writes the image to a temp file
/// and calls `tesseract <file> stdout -l eng`, and checks the result.
///
/// Tesseract must be installed (on PATH or in the NeuronCite runtime cache)
/// for this test to pass. If Tesseract is missing, the test fails with a
/// descriptive error message directing the user to install it.
///
/// Requires the `ocr` feature to be enabled.
#[cfg(feature = "ocr")]
#[test]
fn t_ocr_020_tesseract_cli_ocr_produces_text() {
    // Valid 1x1 white RGB PNG image generated with correct zlib compression
    // and CRC checksums. The raw pixel row is: filter=0x00, R=0xFF, G=0xFF,
    // B=0xFF, zlib-compressed with default compression level.
    let minimal_png: &[u8] = &[
        0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
        0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52, // IHDR chunk (13 bytes)
        0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, // width=1, height=1
        0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53, // 8-bit RGB + CRC
        0xDE, 0x00, 0x00, 0x00, 0x0C, 0x49, 0x44, 0x41, // IDAT chunk (12 bytes)
        0x54, 0x78, 0x9C, 0x63, 0xF8, 0xFF, 0xFF, 0x3F, // zlib data
        0x00, 0x05, 0xFE, 0x02, 0xFE, 0x0D, 0xEF, 0x46, // adler32 + CRC
        0xB8, 0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, // IEND chunk
        0x44, 0xAE, 0x42, 0x60, 0x82, // IEND CRC
    ];

    let result = neuroncite_pdf::ocr::ocr_page(minimal_png, "eng");

    // Skip the test when Tesseract is not installed. On CI runners, Tesseract
    // may not be available, and this test verifies the OCR pipeline rather than
    // testing Tesseract installation. The DepDownload error variant indicates
    // Tesseract is missing from PATH and from the runtime cache.
    let text = match result {
        Ok(t) => t,
        Err(ref e) if format!("{e:?}").contains("DepDownload") => {
            eprintln!("T-OCR-020: skipping -- Tesseract not installed");
            return;
        }
        Err(e) => panic!("OCR failed with unexpected error: {e:?}"),
    };

    // A 1x1 white pixel produces empty or whitespace-only OCR output.
    eprintln!(
        "T-OCR-022: Tesseract CLI returned {} bytes of text",
        text.len()
    );
    assert!(
        text.trim().is_empty() || text.len() < 100,
        "OCR of a single white pixel should produce minimal text, got {} bytes",
        text.len()
    );
}

