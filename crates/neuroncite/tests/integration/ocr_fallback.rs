// ---------------------------------------------------------------------------
// T-OCR-004: Extracted text passes quality heuristics
// ---------------------------------------------------------------------------

/// T-OCR-004: Verifies that the text extracted via the pdfium backend passes
/// the quality heuristics defined in neuroncite_pdf::quality. For each PDF
/// where pdfium extraction succeeds, at least 50% of pages with non-trivial
/// text content must pass all quality thresholds (alphabetic ratio,
/// replacement char ratio, line break density, unique token ratio).
///
/// Requires pdfium.dll / libpdfium.so at runtime.
#[test]
fn t_ocr_004_pdfium_text_passes_quality_checks() {
    assert!(
        is_pdfium_available(),
        "pdfium library is required for this test. Install pdfium via the Doctor panel.",
    );

    let paths = match ocr_pdf_paths() {
        Some(p) => p,
        None => {
            eprintln!("skipping: OCR test PDFs not found. Run: cargo run -p neuroncite-testgen");
            return;
        }
    };
    let mut files_checked = 0_usize;

    for pdf_path in &paths {
        let pages =
            match extract_with_timeout_and_backend(pdf_path, EXTRACT_TIMEOUT, Some("pdfium")) {
                Some(p) => p,
                None => continue,
            };

        // Only check pages that have non-trivial content (>20 chars).
        let non_trivial_pages: Vec<&PageText> = pages
            .iter()
            .filter(|p| p.content.chars().filter(|c| !c.is_whitespace()).count() > 20)
            .collect();

        if non_trivial_pages.is_empty() {
            continue;
        }

        let passing_pages = non_trivial_pages
            .iter()
            .filter(|p| {
                let quality = neuroncite_pdf::quality::check_text_quality(&p.content);
                !neuroncite_pdf::quality::should_fallback(&quality)
            })
            .count();

        let pass_rate = passing_pages as f64 / non_trivial_pages.len() as f64;

        eprintln!(
            "  quality: {}/{} pages pass ({:.0}%) for {}",
            passing_pages,
            non_trivial_pages.len(),
            pass_rate * 100.0,
            pdf_path.file_name().unwrap_or_default().to_string_lossy()
        );

        assert!(
            pass_rate >= 0.50,
            "at least 50% of non-trivial pages must pass quality checks for {}, \
             got {:.0}% ({}/{})",
            pdf_path.display(),
            pass_rate * 100.0,
            passing_pages,
            non_trivial_pages.len()
        );

        files_checked += 1;
    }

    assert!(
        files_checked >= 1,
        "at least 1 OCR PDF must be checked for text quality"
    );
}

// ---------------------------------------------------------------------------
// T-OCR-005: Full pipeline with OCR PDFs stores chunks in SQLite
// ---------------------------------------------------------------------------

/// T-OCR-005: Exercises the complete indexing pipeline (extract -> chunk ->
/// store) against the OCR test PDFs using the pdfium fallback. Verifies that
/// files, pages, and chunks are persisted in SQLite with correct metadata.
///
/// Requires pdfium.dll / libpdfium.so at runtime.
#[test]
fn t_ocr_005_full_pipeline_with_ocr_pdfs() {
    assert!(
        is_pdfium_available(),
        "pdfium library is required for this test. Install pdfium via the Doctor panel.",
    );

    let dir = match ocr_pdf_dir() {
        Some(d) => d,
        None => {
            eprintln!(
                "skipping: OCR test PDF directory not found. Run: cargo run -p neuroncite-testgen"
            );
            return;
        }
    };
    let paths = match ocr_pdf_paths() {
        Some(p) => p,
        None => {
            eprintln!("skipping: OCR test PDFs not found. Run: cargo run -p neuroncite-testgen");
            return;
        }
    };

    let pool = common::create_default_test_pool();
    let conn = pool.get().expect("failed to obtain connection");
    let config = ocr_test_config(&dir);

    let session_id =
        store::create_session(&conn, &config, "0.1.0").expect("session creation failed");

    let strategy = neuroncite_chunk::create_strategy("word", Some(300), Some(50), None, None)
        .expect("strategy creation failed");

    let mut total_files_indexed = 0_usize;
    let mut total_chunks_stored = 0_usize;
    let mut total_pages_extracted = 0_usize;

    for pdf_path in &paths {
        // Use auto mode so the fallback chain engages.
        let pages = match extract_with_timeout_and_backend(pdf_path, EXTRACT_TIMEOUT, None) {
            Some(p) if has_substantial_text(&p, 50) => p,
            _ => continue,
        };

        let chunks = match strategy.chunk(&pages) {
            Ok(c) if !c.is_empty() => c,
            Ok(_) => continue,
            Err(e) => {
                eprintln!("  chunking failed for {}: {e}", pdf_path.display());
                continue;
            }
        };

        total_pages_extracted += pages.len();

        let file_hash = Chunk::compute_content_hash(
            &pages
                .iter()
                .map(|p| p.content.as_str())
                .collect::<Vec<_>>()
                .join(""),
        );

        let file_id = store::insert_file(
            &conn,
            session_id,
            &pdf_path.to_string_lossy(),
            &file_hash,
            1_700_000_000,
            std::fs::metadata(pdf_path)
                .map(|m| m.len() as i64)
                .unwrap_or(0),
            pages.len() as i64,
            None,
        )
        .expect("file insert failed");

        let pages_data: Vec<(i64, &str, &str)> = pages
            .iter()
            .map(|p| {
                (
                    p.page_number as i64,
                    p.content.as_str(),
                    match p.backend {
                        ExtractionBackend::PdfExtract => "pdf-extract",
                        ExtractionBackend::Pdfium => "pdfium",
                        ExtractionBackend::Ocr => "ocr",
                        ExtractionBackend::HtmlRaw => "html-raw",
                        ExtractionBackend::HtmlReadability => "html-readability",
                    },
                )
            })
            .collect();
        store::bulk_insert_pages(&conn, file_id, &pages_data).expect("page insert failed");

        let stub_embedding: Vec<u8> = vec![0u8; 16];
        let chunk_inserts: Vec<ChunkInsert<'_>> = chunks
            .iter()
            .map(|c| ChunkInsert {
                file_id,
                session_id,
                page_start: c.page_start as i64,
                page_end: c.page_end as i64,
                chunk_index: c.chunk_index as i64,
                doc_text_offset_start: c.doc_text_offset_start as i64,
                doc_text_offset_end: c.doc_text_offset_end as i64,
                content: &c.content,
                embedding: Some(stub_embedding.as_slice()),
                ext_offset: None,
                ext_length: None,
                content_hash: &c.content_hash,
                simhash: None,
            })
            .collect();

        let ids = store::bulk_insert_chunks(&conn, &chunk_inserts).expect("chunk insert failed");
        total_chunks_stored += ids.len();
        total_files_indexed += 1;
    }

    assert!(
        total_files_indexed >= 1,
        "at least 1 OCR PDF must be successfully indexed"
    );
    assert!(
        total_chunks_stored >= 1,
        "at least 1 chunk must be stored from OCR PDFs"
    );
    assert!(
        total_pages_extracted >= 1,
        "at least 1 page must be extracted from OCR PDFs"
    );

    // Verify database consistency.
    let files = store::list_files_by_session(&conn, session_id).expect("file listing failed");
    assert_eq!(files.len(), total_files_indexed);

    let all_chunks =
        store::list_chunks_by_session(&conn, session_id).expect("chunk listing failed");
    assert_eq!(all_chunks.len(), total_chunks_stored);

    eprintln!(
        "OCR pipeline: {total_files_indexed} files, \
         {total_pages_extracted} pages, {total_chunks_stored} chunks"
    );
}

