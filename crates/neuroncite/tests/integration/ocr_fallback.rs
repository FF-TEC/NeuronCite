// Integration tests for the pdfium fallback extraction path.
//
// This module tests three synthetic PDFs (generated by neuroncite-testgen)
// that the default pdf-extract backend cannot parse. Each PDF lives in a
// subfolder named after the specific failure mode it triggers:
//
// - cross-reference-error/  Synthetic PDF with corrupted xref table.
//   pdf-extract returns an error; pdfium also fails (triple corruption).
//
// - cid-range-panic/  Synthetic PDF with malformed CID font CMap stream.
//   pdf-extract panics in the CID-range font table parser.
//
// - type1-font-panic/  Synthetic PDF with malformed Type1 FontFile stream.
//   pdf-extract panics in the Type1 font encoding data parser.
//
// The CID-range and Type1 PDFs contain Helvetica-rendered text that pdfium
// extracts after the fallback chain catches the pdf-extract panic. The
// xref-corruption PDF defeats both backends.
//
// Regenerate fixtures: cargo run -p neuroncite-testgen
//
// This module is compiled when the "pdfium" feature is enabled (see
// integration/mod.rs). At runtime, the tests require pdfium.dll /
// libpdfium.so to be available.

use std::path::{Path, PathBuf};
use std::sync::mpsc;
use std::time::Duration;

use neuroncite_core::{Chunk, ExtractionBackend, IndexConfig, PageText, StorageMode};
use neuroncite_store::{self as store, ChunkInsert};

use crate::common;

/// Per-file timeout for PDF text extraction. Scanned and OCR-heavy PDFs may
/// take longer than typical text-based PDFs. 60 seconds is a conservative
/// upper bound that avoids false timeouts on slow machines while still
/// catching infinite loops.
const EXTRACT_TIMEOUT: Duration = Duration::from_secs(60);

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

/// Mapping of each problematic PDF to its subfolder name under tests/PDFs/ocr/.
/// The subfolder name describes the specific pdf-extract failure mode.
const OCR_PDF_ENTRIES: [(&str, &str); 3] = [
    (
        "cross-reference-error",
        "synthetic_statistics_xref_corruption.pdf",
    ),
    ("cid-range-panic", "synthetic_finance_cid_range.pdf"),
    (
        "type1-font-panic",
        "synthetic_forecasting_type1_encoding.pdf",
    ),
];

/// Returns the absolute path to the `tests/PDFs/ocr/` directory containing
/// real-world PDFs that require pdfium or OCR fallback for text extraction.
/// Returns `None` when the directory does not exist (the OCR test corpus
/// has not been provisioned in this environment).
fn ocr_pdf_dir() -> Option<PathBuf> {
    let manifest_dir = Path::new(env!("CARGO_MANIFEST_DIR"));
    let workspace_root = manifest_dir
        .parent() // crates/
        .and_then(|p| p.parent()) // workspace root
        .expect("failed to resolve workspace root from CARGO_MANIFEST_DIR");
    let pdf_dir = workspace_root.join("tests").join("PDFs").join("ocr");
    if pdf_dir.is_dir() {
        Some(pdf_dir)
    } else {
        None
    }
}

/// Returns the absolute paths to all three OCR test PDFs, or `None` when
/// the OCR test directory or any of the expected PDF files is missing.
/// Each PDF resides in a subfolder named after its pdf-extract failure mode.
fn ocr_pdf_paths() -> Option<Vec<PathBuf>> {
    let dir = ocr_pdf_dir()?;
    let mut paths = Vec::with_capacity(OCR_PDF_ENTRIES.len());
    for (subfolder, filename) in &OCR_PDF_ENTRIES {
        let path = dir.join(subfolder).join(filename);
        if !path.is_file() {
            return None;
        }
        paths.push(path);
    }
    Some(paths)
}

/// Returns the absolute path to a specific OCR test PDF by its problem
/// subfolder name (e.g., "cross-reference-error", "cid-range-panic",
/// "type1-font-panic"). Returns `None` when the directory or file is missing.
fn ocr_pdf_by_problem(problem: &str) -> Option<PathBuf> {
    let dir = ocr_pdf_dir()?;
    let (_, filename) = OCR_PDF_ENTRIES.iter().find(|(sub, _)| *sub == problem)?;
    let path = dir.join(problem).join(filename);
    if path.is_file() { Some(path) } else { None }
}

/// Returns an IndexConfig for OCR pipeline tests using word-window chunking.
fn ocr_test_config(directory: &Path) -> IndexConfig {
    IndexConfig {
        directory: directory.to_path_buf(),
        model_name: "stub-model".to_string(),
        chunk_strategy: "word".to_string(),
        chunk_size: Some(300),
        chunk_overlap: Some(50),
        max_words: None,
        ocr_language: "eng".to_string(),
        embedding_storage_mode: StorageMode::SqliteBlob,
        vector_dimension: 4,
    }
}

/// Extracts pages from a PDF file with a per-file timeout and panic catching.
/// Returns `Some(pages)` on success, `None` on error/panic/timeout. The
/// `backend` parameter is forwarded to `neuroncite_pdf::extract_pages` as
/// the backend preference.
fn extract_with_timeout_and_backend(
    pdf_path: &Path,
    timeout: Duration,
    backend: Option<&'static str>,
) -> Option<Vec<PageText>> {
    let path = pdf_path.to_path_buf();
    let (tx, rx) = mpsc::channel();

    // Use 8 MB stack to accommodate pdfium FFI C-library call frames during
    // PDF font parsing (CID-range, Type1). The default ~2 MB thread stack
    // overflows when multiple pdfium extractions run concurrently in the test
    // harness because each pdfium FFI call chain consumes ~1-2 MB of stack.
    std::thread::Builder::new()
        .stack_size(8 * 1024 * 1024)
        .spawn(move || {
            let result = std::panic::catch_unwind(move || {
                neuroncite_pdf::extract_pages(&path, backend, None)
            });
            let _ = tx.send(result);
        })
        .expect("failed to spawn extraction thread");

    match rx.recv_timeout(timeout) {
        Ok(Ok(Ok(extraction))) => Some(extraction.pages),
        Ok(Ok(Err(e))) => {
            eprintln!("  extraction error for {}: {e}", pdf_path.display());
            None
        }
        Ok(Err(_)) => {
            eprintln!(
                "  extraction panicked for {} (third-party font parser)",
                pdf_path.display()
            );
            None
        }
        Err(_) => {
            eprintln!(
                "  extraction timed out for {} after {timeout:?}",
                pdf_path.display()
            );
            None
        }
    }
}

/// Returns true if the page list contains at least one page with more
/// than `min_chars` non-whitespace characters.
fn has_substantial_text(pages: &[PageText], min_chars: usize) -> bool {
    pages
        .iter()
        .any(|p| p.content.chars().filter(|c| !c.is_whitespace()).count() > min_chars)
}

/// Checks whether pdfium is available at runtime by writing a minimal
/// valid PDF to a temporary file and attempting pdfium extraction on it.
/// Returns true if pdfium.dll / libpdfium.so was loaded successfully.
///
/// The probe creates a zero-page PDF to minimize I/O overhead. If the
/// pdfium shared library cannot be loaded, the error message will contain
/// "bind to pdfium" or "LoadLibrary". Any other error (PDF parsing, etc.)
/// indicates that pdfium IS loaded but the probe PDF was rejected.
fn is_pdfium_available() -> bool {
    use std::io::Write;

    // Minimal valid PDF structure: catalog and empty page tree.
    let probe_pdf = b"%PDF-1.0\n\
        1 0 obj\n<< /Type /Catalog /Pages 2 0 R >>\nendobj\n\
        2 0 obj\n<< /Type /Pages /Kids [] /Count 0 >>\nendobj\n\
        xref\n0 3\n\
        0000000000 65535 f \n\
        0000000009 00000 n \n\
        0000000058 00000 n \n\
        trailer\n<< /Size 3 /Root 1 0 R >>\nstartxref\n0\n%%EOF";

    let tmp = match tempfile::NamedTempFile::with_suffix(".pdf") {
        Ok(mut t) => {
            if t.write_all(probe_pdf).is_err() || t.flush().is_err() {
                return false;
            }
            t
        }
        Err(_) => return false,
    };

    match neuroncite_pdf::extract_pages(tmp.path(), Some("pdfium"), None) {
        Ok(_) => true,
        Err(e) => {
            let msg = format!("{e}");
            // If the error is about loading the pdfium shared library,
            // pdfium is not installed on this system.
            let is_binding_error = msg.contains("bind to pdfium") || msg.contains("LoadLibrary");
            !is_binding_error
        }
    }
}

