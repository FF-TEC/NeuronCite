// ---------------------------------------------------------------------------
// T-OCR-010: Synthetic xref corruption - cross-reference table error
// ---------------------------------------------------------------------------

/// T-OCR-010: The synthetic xref-corruption PDF has triple-corrupted cross-
/// reference data (xref offsets, startxref pointer, trailer /Size) that
/// causes both pdf-extract and pdfium to fail. This PDF requires OCR
/// (Tesseract) for text extraction. Without OCR, both backends fail and
/// the fallback chain correctly propagates the error.
///
/// This test verifies:
/// 1. pdf-extract fails (cross-reference error).
/// 2. pdfium also fails (FormatError from the same corruption).
/// 3. auto-extract returns an error (both backends exhausted, no OCR).
#[test]
fn t_ocr_010_cross_reference_error_openintro() {
    assert!(
        is_pdfium_available(),
        "pdfium library is required for this test. Install pdfium via the Doctor panel.",
    );

    let path = match ocr_pdf_by_problem("cross-reference-error") {
        Some(p) => p,
        None => {
            eprintln!("skipping: OCR test PDF not found. Run: cargo run -p neuroncite-testgen");
            return;
        }
    };

    // Property 1: pdf-extract fails for this PDF.
    let pdf_extract_result =
        extract_with_timeout_and_backend(&path, EXTRACT_TIMEOUT, Some("pdf-extract"));
    match pdf_extract_result {
        Some(pages) => {
            let non_ws: usize = pages
                .iter()
                .map(|p| p.content.chars().filter(|c| !c.is_whitespace()).count())
                .sum();
            assert!(
                non_ws < 500,
                "pdf-extract should produce minimal text for cross-reference-error PDF, got {non_ws} chars"
            );
        }
        None => {
            // Expected: pdf-extract returned an error.
        }
    }

    // Property 2: pdfium also fails for this PDF (FormatError due to
    // severely corrupted cross-reference table).
    let pdfium_result = extract_with_timeout_and_backend(&path, EXTRACT_TIMEOUT, Some("pdfium"));
    assert!(
        pdfium_result.is_none(),
        "pdfium should also fail for xref-corruption PDF (triple corruption \
         eliminates all repair anchor points for both backends)"
    );

    // Property 3: auto-extract returns an error because both backends fail
    // and OCR is not enabled in this build. The error should not panic.
    let auto_result = extract_with_timeout_and_backend(&path, EXTRACT_TIMEOUT, None);
    if let Some(pages) = auto_result {
        // If auto mode somehow produced pages (e.g., OCR was available),
        // they should contain meaningful text.
        let non_ws: usize = pages
            .iter()
            .map(|p| p.content.chars().filter(|c| !c.is_whitespace()).count())
            .sum();
        eprintln!(
            "auto-extract produced {non_ws} non-whitespace chars for xref-corruption PDF \
             (OCR may be available)"
        );
    } else {
        // Expected: both text backends failed, no OCR available.
        eprintln!(
            "auto-extract correctly returned error for xref-corruption PDF \
             (both backends failed, OCR not available)"
        );
    }
}

