// T-OCR-001: pdf-extract fails for all 3 OCR PDFs
// ---------------------------------------------------------------------------

/// T-OCR-001: Confirms that the default pdf-extract backend cannot extract
/// text from the three OCR test PDFs. Each file either returns an error or
/// triggers a panic (caught by catch_unwind). This test validates the
/// premise that these PDFs require fallback backends.
#[test]
fn t_ocr_001_pdf_extract_fails_for_ocr_pdfs() {
    let paths = match ocr_pdf_paths() {
        Some(p) => p,
        None => {
            eprintln!("skipping: OCR test PDFs not found. Run: cargo run -p neuroncite-testgen");
            return;
        }
    };

    for pdf_path in &paths {
        let result =
            extract_with_timeout_and_backend(pdf_path, EXTRACT_TIMEOUT, Some("pdf-extract"));

        match result {
            Some(pages) => {
                // pdf-extract may return pages but with no usable text.
                // Check that the text quality is insufficient.
                let total_non_ws: usize = pages
                    .iter()
                    .map(|p| p.content.chars().filter(|c| !c.is_whitespace()).count())
                    .sum();
                eprintln!(
                    "  pdf-extract returned {} pages ({} non-ws chars) for {}",
                    pages.len(),
                    total_non_ws,
                    pdf_path.file_name().unwrap_or_default().to_string_lossy()
                );
                // It is acceptable for pdf-extract to return pages with
                // zero or very little text (quality heuristics would trigger
                // fallback in auto mode). The key property is that the text
                // is NOT sufficient for meaningful search.
            }
            None => {
                // Expected: extraction failed or panicked.
                eprintln!(
                    "  pdf-extract failed/panicked for {} (expected)",
                    pdf_path.file_name().unwrap_or_default().to_string_lossy()
                );
            }
        }
    }
}

// ---------------------------------------------------------------------------
// T-OCR-002: Pdfium fallback extracts text from all 3 PDFs
// ---------------------------------------------------------------------------

/// T-OCR-002: Verifies that the pdfium backend successfully extracts text
/// from all three OCR test PDFs when called directly. These PDFs have
/// broken cross-reference tables, malformed CID-range entries, or corrupt
/// Type1 font data that pdf-extract cannot handle, but pdfium's Chrome-
/// derived PDF engine parses correctly.
///
/// This test requires pdfium.dll / libpdfium.so at runtime. If the library
/// is not available, the test fails with a descriptive assertion message.
#[test]
fn t_ocr_002_pdfium_fallback_extracts_text() {
    assert!(
        is_pdfium_available(),
        "pdfium library is required for this test. Install pdfium via the Doctor panel.",
    );

    let paths = match ocr_pdf_paths() {
        Some(p) => p,
        None => {
            eprintln!("skipping: OCR test PDFs not found. Run: cargo run -p neuroncite-testgen");
            return;
        }
    };
    let mut files_with_text = 0_usize;

    for pdf_path in &paths {
        let result = extract_with_timeout_and_backend(pdf_path, EXTRACT_TIMEOUT, Some("pdfium"));

        match result {
            Some(pages) => {
                let total_non_ws: usize = pages
                    .iter()
                    .map(|p| p.content.chars().filter(|c| !c.is_whitespace()).count())
                    .sum();

                eprintln!(
                    "  pdfium: {} pages, {} non-ws chars for {}",
                    pages.len(),
                    total_non_ws,
                    pdf_path.file_name().unwrap_or_default().to_string_lossy()
                );

                assert!(
                    !pages.is_empty(),
                    "pdfium must return at least one page for {}",
                    pdf_path.display()
                );

                // Verify backend annotation is Pdfium for all pages.
                for page in &pages {
                    assert_eq!(
                        page.backend,
                        ExtractionBackend::Pdfium,
                        "backend must be Pdfium for page {} of {}",
                        page.page_number,
                        pdf_path.display()
                    );
                }

                if has_substantial_text(&pages, 100) {
                    files_with_text += 1;
                }
            }
            None => {
                eprintln!(
                    "  pdfium extraction failed for {}",
                    pdf_path.file_name().unwrap_or_default().to_string_lossy()
                );
            }
        }
    }

    assert!(
        files_with_text >= 2,
        "pdfium must extract substantial text from at least 2 of 3 OCR PDFs, got {files_with_text}"
    );
}

