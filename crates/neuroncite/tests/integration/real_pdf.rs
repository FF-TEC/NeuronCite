// T-REAL-003: Malformed PDFs handled gracefully
// ---------------------------------------------------------------------------

/// T-REAL-003: Verifies that the synthetic PDFs with malformed fonts are
/// handled without panics or hangs. Each PDF is extracted sequentially with
/// per-file timeout protection. Each file is classified by text density.
#[test]
fn t_real_003_scanned_pdfs_handled_gracefully() {
    let _lock = common::acquire_heavy_test_lock();
    let pdf_dir = match synthetic_pdf_dir() {
        Some(d) => d,
        None => {
            eprintln!("skipping: synthetic PDF directory not found");
            return;
        }
    };
    let discovered = neuroncite_pdf::discover_pdfs(&pdf_dir).expect("PDF discovery failed");

    enum PdfClass {
        Text,
        Scanned,
        Error,
    }

    let classes: Vec<PdfClass> = discovered
        .iter()
        .map(
            |pdf_path| match extract_with_timeout(pdf_path, EXTRACT_TIMEOUT) {
                Some(pages) => {
                    let all_sparse = pages
                        .iter()
                        .all(|p| p.content.chars().filter(|c| !c.is_whitespace()).count() < 20);
                    if pages.is_empty() || all_sparse {
                        PdfClass::Scanned
                    } else {
                        PdfClass::Text
                    }
                }
                None => PdfClass::Error,
            },
        )
        .collect();

    let text_count = classes
        .iter()
        .filter(|c| matches!(c, PdfClass::Text))
        .count();
    let scanned_count = classes
        .iter()
        .filter(|c| matches!(c, PdfClass::Scanned))
        .count();
    let error_count = classes
        .iter()
        .filter(|c| matches!(c, PdfClass::Error))
        .count();

    eprintln!(
        "PDF classification: text={text_count}, scanned={scanned_count}, errors={error_count}"
    );

    // The xref-corruption PDF causes extraction errors; the CID-range and
    // Type1 PDFs produce text via the pdfium fallback. At least one must
    // yield text.
    assert!(
        text_count + error_count > 0,
        "at least one PDF must be processed (text or graceful error)"
    );
}

