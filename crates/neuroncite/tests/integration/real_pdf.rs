/// Per-file timeout for PDF text extraction. If a single PDF takes longer
/// than this to extract, it is skipped.
const EXTRACT_TIMEOUT: Duration = Duration::from_secs(30);

// ---------------------------------------------------------------------------
// Helper: resolve the synthetic PDF test directory
// ---------------------------------------------------------------------------

/// Returns the absolute path to the `tests/PDFs/ocr/` directory containing
/// the synthetic test PDFs generated by neuroncite-testgen. The path is
/// resolved relative to the neuroncite binary crate's manifest directory.
///
/// Returns `None` if the directory does not exist (PDFs not yet generated).
fn synthetic_pdf_dir() -> Option<PathBuf> {
    let manifest_dir = Path::new(env!("CARGO_MANIFEST_DIR"));
    let workspace_root = manifest_dir
        .parent()
        .and_then(|p| p.parent())
        .expect("failed to resolve workspace root from CARGO_MANIFEST_DIR");
    let pdf_dir = workspace_root.join("tests").join("PDFs").join("ocr");
    if pdf_dir.is_dir() {
        Some(pdf_dir)
    } else {
        None
    }
}

/// Returns an IndexConfig suitable for pipeline tests with word-window
/// chunking (300 words, 50 overlap).
fn pipeline_config(directory: &Path) -> IndexConfig {
    IndexConfig {
        directory: directory.to_path_buf(),
        model_name: "stub-model".to_string(),
        chunk_strategy: "word".to_string(),
        chunk_size: Some(300),
        chunk_overlap: Some(50),
        max_words: None,
        ocr_language: "eng".to_string(),
        embedding_storage_mode: StorageMode::SqliteBlob,
        vector_dimension: 4,
    }
}

/// Extracts pages from a PDF file with a per-file timeout. Runs the
/// extraction in a separate thread and uses a channel with `recv_timeout`
/// to enforce the deadline. Catches panics from the third-party
/// `pdf-extract` crate's font parser that occur on malformed font encodings.
///
/// Returns `Some(pages)` on success, `None` on extraction failure, panic,
/// or timeout.
fn extract_with_timeout(pdf_path: &Path, timeout: Duration) -> Option<Vec<PageText>> {
    let path = pdf_path.to_path_buf();
    let (tx, rx) = mpsc::channel();

    // Use 8 MB stack to accommodate pdfium FFI C-library call frames during
    // PDF font parsing (CID-range, Type1).
    std::thread::Builder::new()
        .stack_size(8 * 1024 * 1024)
        .spawn(move || {
            let result =
                std::panic::catch_unwind(move || neuroncite_pdf::extract_pages(&path, None, None));
            let _ = tx.send(result);
        })
        .expect("failed to spawn extraction thread");

    match rx.recv_timeout(timeout) {
        Ok(Ok(Ok(extraction))) => Some(extraction.pages),
        Ok(Ok(Err(e))) => {
            eprintln!("  extraction error for {}: {e}", pdf_path.display());
            None
        }
        Ok(Err(_)) => {
            eprintln!(
                "  extraction panicked for {} (third-party font parser)",
                pdf_path.display()
            );
            None
        }
        Err(_) => {
            eprintln!(
                "  extraction timed out for {} after {timeout:?}",
                pdf_path.display()
            );
            None
        }
    }
}

/// Returns true if the page list contains at least one page with more
/// than `min_chars` non-whitespace characters.
fn has_substantial_text(pages: &[PageText], min_chars: usize) -> bool {
    pages
        .iter()
        .any(|p| p.content.chars().filter(|c| !c.is_whitespace()).count() > min_chars)
}

/// Result of parallel extraction and chunking of a single PDF file.
struct ExtractedFile {
    pdf_path: PathBuf,
    pages: Vec<PageText>,
    chunks: Vec<neuroncite_core::Chunk>,
}

/// Extracts and chunks a single PDF file. Uses `extract_with_timeout` for
/// panic and timeout protection, then applies the chunking strategy.
fn extract_and_chunk(pdf_path: &Path, strategy: &dyn ChunkStrategy) -> Option<ExtractedFile> {
    let pages = extract_with_timeout(pdf_path, EXTRACT_TIMEOUT)?;

    if !has_substantial_text(&pages, 50) {
        return None;
    }

    let chunks = match strategy.chunk(&pages) {
        Ok(c) if !c.is_empty() => c,
        Ok(_) => return None,
        Err(e) => {
            eprintln!("  chunking failed for {}: {e}", pdf_path.display());
            return None;
        }
    };

    Some(ExtractedFile {
        pdf_path: pdf_path.to_path_buf(),
        pages,
        chunks,
    })
}

