// Integration tests using the synthetic PDF fixtures from neuroncite-testgen.
//
// This module exercises the complete indexing pipeline against the synthetic
// test PDFs in tests/PDFs/ocr/: PDF discovery, text extraction (with
// fallback for malformed fonts), word-window chunking, sentence chunking,
// page-based chunking, SQLite storage with FTS5 population, and HNSW
// index construction.
//
// The synthetic PDFs replace the copyrighted academic papers that were
// previously used. Two of the three synthetic PDFs (CID-range and Type1)
// contain extractable Helvetica text; the third (xref-corruption) is
// intentionally unreadable by both pdf-extract and pdfium.
//
// Every call to `extract_pages` is wrapped in `extract_with_timeout`,
// which runs extraction in a dedicated thread with a per-file timeout.
// This prevents the test suite from hanging when the third-party
// `pdf-extract` crate enters an infinite loop on certain font encodings.
//
// All tests in this module acquire `common::acquire_heavy_test_lock()`
// at entry. This serializes execution so that at most one pipeline test
// runs at a time.

use std::path::{Path, PathBuf};
use std::sync::mpsc;
