/// T-REAL-008: Verifies that the page-based chunking strategy produces one
/// chunk per non-empty page from synthetic PDF content.
#[test]
fn t_real_008_page_chunking_on_real_content() {
    let _lock = common::acquire_heavy_test_lock();
    let pdf_dir = match synthetic_pdf_dir() {
        Some(d) => d,
        None => {
            eprintln!("skipping: synthetic PDF directory not found");
            return;
        }
    };
    let discovered = neuroncite_pdf::discover_pdfs(&pdf_dir).expect("PDF discovery failed");

    let strategy = neuroncite_chunk::create_strategy("page", None, None, None, None)
        .expect("page strategy creation failed");

    for pdf_path in &discovered {
        let pages = match extract_with_timeout(pdf_path, EXTRACT_TIMEOUT) {
            Some(p) => p,
            None => continue,
        };

        if pages.is_empty() {
            continue;
        }

        let chunks = strategy.chunk(&pages).expect("page chunking failed");

        let non_empty_pages = pages
            .iter()
            .filter(|p| !p.content.trim().is_empty())
            .count();

        assert_eq!(
            chunks.len(),
            non_empty_pages,
            "page chunking must produce one chunk per non-empty page for {}",
            pdf_path.display()
        );

        for chunk in &chunks {
            assert!(chunk.page_start >= 1, "chunk page_start must be >= 1");
            assert_eq!(
                chunk.page_start, chunk.page_end,
                "page-based chunks must span one page"
            );
        }

        eprintln!(
            "page chunking {}: {} pages -> {} chunks",
            pdf_path.file_name().unwrap_or_default().to_string_lossy(),
            pages.len(),
            chunks.len()
        );

        break;
    }
}

